export class Product {
  constructor(
    name,
    description,
    price,
    image,
    brand,
    category,
    rating,
    reviewers,
    tags
  ) {
    this.name = name;
    this.description = description;
    this.price = price;
    this.image = image;
    this.brand = brand;
    this.category = category;
    this.rating = rating;
    this.reviewers = reviewers;
    this.tags = tags;
  }
}
